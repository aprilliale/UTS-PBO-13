    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        SimpleDateFormat dateformat = new SimpleDateFormat("yyy-MM-dd");
        String pinjam = dateformat.format(tglPinjam.getDate());
        String kembali = dateformat.format(Jkembali.getDate());
        if("".equals(anggota.getText()) || "".equals(nama.getText()) || "".equals(tbl.getText())
            || "".equals(judul.getText()) || pinjam.equals("") || kembali.equals(""))
        {
            JOptionPane.showMessageDialog(this, "Harap lengkapi Data", "Error", JOptionPane.WARNING_MESSAGE);
        }
        else {
            String jenis="";
            if (fiksi.isSelected()){
                jenis = "Fiksi";
            }else if (edukasi.isSelected()){
                jenis = "Edukasi";
            }else{
                jenis = "etc";
            }
            
            String sta="";
            if(telat.isSelected()){
                JOptionPane.showMessageDialog(this, "Anda telat!!! Silakan Bayar Denda "
                        + "Rp. 5000", "WARNING", JOptionPane.WARNING_MESSAGE);
                sta="Telat";
            }else{
                jenis ="Tepat";
            }
            
            String SQL = "INSERT INTO peminjam (no,nama,alamat,jenis,judul,pinjam,kembali,status) "
                    + "VALUES ('"+anggota.getText()+"', '"+nama.getText()+"', '"+tbl.getText()+"', '"+
                    jenis+"', '"+judul.getText()+"', '"+pinjam+"', '"+kembali+"', '"+sta+"')";
            int status = KoneksiDB.execute(SQL);
            if(status == 1){
                JOptionPane.showMessageDialog(this, "Data berhasil ditambahkan", "Sukses", JOptionPane.INFORMATION_MESSAGE);
                selectData();
            }
            else{
                JOptionPane.showMessageDialog(this, "Data gagal ditambahkan", "Gagal", JOptionPane.WARNING_MESSAGE);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteActionPerformed
        // TODO add your handling code here:
        int baris = tData.getSelectedRow();
        if(baris != -1){
            String no = tData.getValueAt(baris, 0).toString();
            String SQL = "DELETE FROM peminjam WHERE no='"+no+"'";
            int status = KoneksiDB.execute(SQL);
            if(status==1){
                JOptionPane.showMessageDialog(this, "Data berhasil dihapus", "Sukses", JOptionPane.INFORMATION_MESSAGE);
            }else{
                JOptionPane.showMessageDialog(this, "Data gagal dihapus", "Gagal", JOptionPane.WARNING_MESSAGE);
            }
        }else{
            JOptionPane.showMessageDialog(this, "Pilih baris data terlebih dahulu", "Error", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_deleteActionPerformed

