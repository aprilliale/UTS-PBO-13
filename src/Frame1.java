    private void selectData() {
        //throw new UnsupportedOperationException("Not supported yet."); //To change body of generated methods, choose Tools | Templates.
        String kolom[]= {"no","nama","alamat","jenis","judul","pinjam","kembali","status"};
         DefaultTableModel dtm = new DefaultTableModel(null, kolom);
         String SQL = "SELECT * FROM peminjam";
         ResultSet rs = KoneksiDB.executeQuery(SQL);
         try{
             while(rs.next()){
                 String no = rs.getString(1);
                 String nama = rs.getString(2);
                 String alamat = rs.getString(3);
                 String jenis ="";
                 if("Fiksi".equals(rs.getString(4))){
                     jenis = "Fiksi";
                 }else if ("Edukasi".equals(rs.getString(4))){
                     jenis = "Edukasi";
                 }else{
                     jenis ="etc";
                 }
                 
                 String judul = rs.getString(5);
                 String pinjam = rs.getString(6);
                 String kembali = rs.getString(7);
                 String status ="";
                 if("Telat".equals(rs.getString(8))){
                     status="Telat";
                 }else{
                     status="Tepat Waktu";
                 }
                 String data[]= {no,nama,alamat,jenis,judul,pinjam,kembali,status};
                 dtm.addRow(data);
             }
         }catch(SQLException ex){
             Logger.getLogger(Frame1.class.getName()).log(Level.SEVERE, null, ex);
         }
         tData.setModel(dtm);
    }

