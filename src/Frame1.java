    private void tDataMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tDataMouseClicked
        // TODO add your handling code here:
        int baris = tData.getSelectedRow();
        if(baris != -1){
            anggota.setText(tData.getValueAt(baris, 0).toString());
            nama.setText(tData.getValueAt(baris, 1).toString());
            tbl.setText(tData.getValueAt(baris, 2).toString());
            if("Fiksi".equals(tData.getValueAt(baris, 3).toString())){
                fiksi.setSelected(true);
            }else if("Edukasi".equals(tData.getValueAt(baris, 3).toString())){
                edukasi.setSelected(true);
            }else{
                etc.setSelected(true);
            }
            judul.setText(tData.getValueAt(baris, 4).toString());
            //tempatlahir.setText(tData.getValueAt(baris, 4).toString());
            SimpleDateFormat date = new SimpleDateFormat("yyyy-MM-dd");
            Date dateformat = null;
            try{
                   dateformat = date.parse(tData.getValueAt(baris, 5).toString());
            } catch (ParseException ex){
                Logger.getLogger(Frame1.class.getName()).log(Level.SEVERE, null, ex);
            }
            tglPinjam.setDate(dateformat);
            SimpleDateFormat tg = new SimpleDateFormat("yyyy-MM-dd");
            Date tag =null;
            try{
                   tag = tg.parse(tData.getValueAt(baris, 6).toString());
            } catch (ParseException ex){
                Logger.getLogger(Frame1.class.getName()).log(Level.SEVERE, null, ex);
            }
            Jkembali.setDate(tag);
             if("Telat".equals(tData.getValueAt(baris, 7).toString())){
                telat.setSelected(true);
            }else{
                tepat.setSelected(true);
            }
        }
    }//GEN-LAST:event_tDataMouseClicked

